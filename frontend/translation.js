function setLanguage(lang) {
  const elements = document.querySelectorAll('[data-translate]');
  elements.forEach(element => {
    const key = element.getAttribute('data-translate');
    if (translations[lang] && translations[lang][key]) {
      if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
        element.placeholder = translations[lang][key];
      } else {
        element.textContent = translations[lang][key];
      }
    }
  });
  
  // Update language selector
  const currentLangElement = document.getElementById('currentLang');
  if (currentLangElement) {
    currentLangElement.textContent = getLanguageName(lang);
  }
  
  // Save language preference
  localStorage.setItem('agri_language', lang);
  
  // Dispatch event for chatbot to update
  document.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
}

function getLanguageName(code) {
  const languages = {
    'en': 'English',
    'hi': 'हिन्दी',
    'te': 'తెలుగు',
    'ta': 'தமிழ்',
    'bn': 'বাংলা',
    'mr': 'मराठी'
  };
  return languages[code] || 'English';
}

// Initialize language when DOM is loaded
document.addEventListener('DOMContentLoaded', function() {
  const savedLang = localStorage.getItem('agri_language') || 'en';
  setLanguage(savedLang);
  
  // Add event listeners to language selector
  document.querySelectorAll('.lang-option').forEach(option => {
    option.addEventListener('click', function(e) {
      e.preventDefault();
      const lang = this.getAttribute('data-lang');
      setLanguage(lang);
    });
  });
});